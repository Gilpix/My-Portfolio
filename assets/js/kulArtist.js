//Get value for selected project from ProjectDetails objec
function showModal(no){
	$(".card-button").click(function(){
		var id=projectDetails[no].id;
		var name =projectDetails[no].name;
	var shortDesc =projectDetails[no].heading;
	var description =projectDetails[no].description;
	var demo=projectDetails[no].demo;
	var code=projectDetails[no].code;
	var image =projectDetails[no].image;
	modal(id,name,shortDesc, description,demo,code,image);
	})
}

//Show Modal for selected project
function modal(id,name,shortDesc, description,demo,code,image){
	$(".modal-heading").text(name);
	$(".modal-short-text").text(shortDesc);
	$(".modal-desc-text").html(description);
	$(".project-demo").attr("href",demo);
	$(".project-code").attr("href",code);
	$(".modal-image").attr("src","assets/res/"+image);
	if(id==9 || id==8)
	{
		$(".project-code").addClass("hide-cards");
		$(".project-demo").addClass("hide-cards");
	}
	else if(id==2 || id==3)
	{
		$(".project-demo").addClass("hide-cards");
	}
	else
	{
		$(".project-code").removeClass("hide-cards");
		$(".project-demo").removeClass("hide-cards");
	}
	$(".myModal").modal('show');
}


